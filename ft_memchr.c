#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	while (n--)
	{
		if (*(unsigned char *)str == (char)c)
			return ((unsigned char *)str);
		str = (unsigned char *)str + 1;
	}
	return (NULL);
}
